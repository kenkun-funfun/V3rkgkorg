import { createSignal } from 'solid-js';

export type Lang = 'ja' | 'en' | 'ko' | 'th' | 'id' | 'zh-CN' | 'zh-TW';

// ✅ 初期言語を決定（localStorage優先 → ブラウザ設定）
function detectInitialLang(): Lang {
    const stored = localStorage.getItem('lang');
    if (
        stored === 'ja' ||
        stored === 'en' ||
        stored === 'ko' ||
        stored === 'th' ||
        stored === 'id' ||
        stored === 'zh-CN' ||
        stored === 'zh-TW'
    ) {
        return stored as Lang;
    }

    const browserLang = navigator.language.toLowerCase();
    if (browserLang.startsWith('ja')) return 'ja';
    if (browserLang.startsWith('ko')) return 'ko';
    if (browserLang.startsWith('th')) return 'th';
    if (browserLang.startsWith('id')) return 'id';
    if (browserLang === 'zh-tw') return 'zh-TW';
    if (browserLang === 'zh-cn' || browserLang.startsWith('zh')) return 'zh-CN';

    return 'en'; // fallback
}



// ✅ Signal定義
const [lang, setLangSignal] = createSignal<Lang>(detectInitialLang());

// ✅ 外部用 setter（localStorage保存付き）
export function setLang(newLang: Lang) {
    localStorage.setItem('lang', newLang);
    setLangSignal(newLang);
}

